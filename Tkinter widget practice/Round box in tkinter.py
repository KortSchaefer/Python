from tkinter import *

def round_rectangle(x1, y1, x2, y2, radius=25, **kwargs):
    points = [x1+radius, y1,
              x1+radius, y1,
              x2-radius, y1,
              x2-radius, y1,
              x2, y1,
              x2, y1+radius,
              x2, y1+radius,
              x2, y2-radius,
              x2, y2-radius,
              x2, y2,
              x2-radius, y2,
              x2-radius, y2,
              x1+radius, y2,
              x1+radius, y2,
              x1, y2,
              x1, y2-radius,
              x1, y2-radius,
              x1, y1+radius,
              x1, y1+radius,
              x1, y1]
    return canvas.create_polygon(points, **kwargs, smooth=True)

root = Tk()

# Create a canvas
canvas = Canvas(root, width=300, height=200)
canvas.grid(column=0, row=0, padx=20, pady=20)

# Create a rounded rectangle
round_rectangle(10, 10, 290, 190, radius=50, fill="lightblue")

root.mainloop()